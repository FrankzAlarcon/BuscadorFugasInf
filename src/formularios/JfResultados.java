    public void agregarDatosLeaked(String nomAlgortimo,long tiempoE, String numVeces){
        String [] Datos= new String[4];
        Datos[0] = nomAlgortimo;
        Datos[1] = ""+tiempoE;
        Datos[2] = numVeces;
        this.modelo.addRow(Datos); //Anadimos los datos
        
        jTResultados1.getColumnModel().getColumn(1).setPreferredWidth(150); //Establecemos en 150 el ancho de la columna 1
        jTResultados1.getColumnModel().getColumn(2).setPreferredWidth(300); //Establecemos en 300 el ancho de la columna 2
        this.jTResultados1.setModel(modelo);
        jTResultados1.setRowHeight(30); //Establecemos la altura de las filas en 30
    }
    
